package Esercizio7;

public class Motore {

	private int cilandrata;
	private int cavalli;
	private int clilindri;

	public Motore(int cilandrata, int cavalli, int clilindri) {
		this.cilandrata = cilandrata;
		this.cavalli = cavalli;
		this.clilindri = clilindri;
	}

	public int getCilandrata() {
		return cilandrata;
	}

	public int getCavalli() {
		return cavalli;
	}

	public int getClilindri() {
		return clilindri;
	}

}
