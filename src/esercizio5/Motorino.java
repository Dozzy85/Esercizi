package esercizio5;

public class Motorino {

	private String colore;
	private int velocità;
	private String tipo;

	public Motorino(String colore, int velocità, String tipo) {
		this.colore = colore;
		this.velocità = velocità;
		this.tipo = tipo;
	}

	public int getVelocità() {
		return velocità;
	}

}
